def get_lang() -> str:
    lang = st.session_state.get("lang", "en")
    return lang if lang in SUPPORTED_LANGS else "en"


def t(key: str, **kwargs) -> str:
    lang = get_lang()
    text = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key, key)
    return text.format(**kwargs) if kwargs else text


def period_label(period_key: str) -> str:
    return t(f"period_{period_key}")


def nav_label(nav_key: str) -> str:
    return t(f"nav_{nav_key}")


def stock_nav_label(nav_key: str, symbol: str = "") -> str:
    label = nav_label(nav_key)
    if symbol and nav_key in STOCK_NAV_KEYS:
        return f"{label}（{symbol}）"
    return label


def stock_title(title_key: str, symbol: str = "") -> str:
    text = t(title_key)
    if symbol:
        return f"{text}（{symbol}）"
    return text


def chart_label(chart_key: str) -> str:
    return t(f"chart_{chart_key}")



def top_sort_label(sort_key: str) -> str:
    return t(f"top_sort_{sort_key}")


def translate_rating_label(label: str) -> str:
    mapping = {
        "强力买入": "rating_strong_buy",
        "买入": "rating_buy",
        "持有": "rating_hold",
        "卖出": "rating_sell",
        "强力卖出": "rating_strong_sell",
    }
    return t(mapping[label]) if label in mapping else label


def init_language():
    if st.session_state.get("lang") not in SUPPORTED_LANGS:
        st.session_state["lang"] = "en"


def render_language_selector():
    init_language()
    st.selectbox(
        t("language"),
        options=list(SUPPORTED_LANGS),
        format_func=lambda code: LANG_LABELS[code],
        key="lang",
    )
